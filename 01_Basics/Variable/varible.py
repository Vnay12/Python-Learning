# Python Variable Casting 
# Casting is when you change an variable data type to another data type.
# There are two types of casting:
# 1. Implicit Casting
# 2. Explicit
# Implicit Casting: Python automatically converts one data type to another data type.
# Explicit Casting: You can convert from one data type to another with the int(), float(), and str() methods.
# Example:
# Convert from int to float:
x = 1
y = 2.8
z = 1
a = float(x)
b = float(y)
c = float(z)
print(a)
print(b)
print(c)

# Convert from float to int:
x = 1.9
y = 2.8
z = 1.9
a = int(x)
b = int(y)
c = int(z)
print(a)
print(b)
print(c)

# Convert from int to complex:
x = 1
y = 2.8
z = 1j
a = complex(x)
b = complex(y)
c = complex(z)
print(a)
print(b)
print(c)

